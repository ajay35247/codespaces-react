package com.speedytrucks.app.designsystem.tokens

import androidx.compose.ui.unit.dp

/**
 * 4dp / 8dp spacing scale.
 *
 * All paddings, gaps, and margins in the app must reference these tokens
 * instead of hardcoded dp values so the spacing system stays consistent and
 * easy to update globally.
 */
object Spacing {
    val xxs = 2.dp
    val xs = 4.dp
    val sm = 8.dp
    val md = 12.dp
    val lg = 16.dp
    val xl = 24.dp
    val xxl = 32.dp
    val xxxl = 48.dp
    val huge = 64.dp
}
